unsigned check_kline_req(uint8_t * data, unsigned len, uint8_t * resp)
{
    if (len != sizeof(kline_request_t)) {
        dp("    DLC req ERR: len != 5\n");
        return 0;
    }

    kline_request_t * req = (kline_request_t *)data;

    if (calc_cs(data, sizeof(kline_request_t)) != 0) {
        dp("    DLC req ERR: wrong cs\n");
        return 0;
    }

    if (req->frame_len != sizeof(kline_request_t)) {
        dp("    DLC req ERR: frame_len != 5\n");
        return 0;
    }

    honda_unit_t hu;
    if (honda_dlc_unit_from_address(req->cmd, &hu) == 0) {
        dp("    DLC req ERR: unknown cmd\n");
        return 0;
    }

    if ((req->offset + req->len) > honda_dlc_unit_len(hu)) {
        dp("    DLC req ERR: out of read memory\n");
        return 0;
    }

    resp[0] = 0;
    resp[1] = req->len + 3;
    str_cp(&resp[2], &honda_units_map[hu][req->offset], req->len);
    resp[req->len + 2] = calc_cs(resp, req->len + 2);

    return req->len + 3;
}
